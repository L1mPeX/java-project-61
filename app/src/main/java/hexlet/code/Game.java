package hexlet.code;

/**
 * Интерфейс для всех игр.
 * Определяет контракт, который должна реализовать каждая игра.
 * Игры отвечают только за формирование данных (вопросы и ответы).
 */
public interface Game {
    /**
     * Возвращает правило игры.
     *
     * @return строка с описанием правила игры
     */
    String getRule();

    /**
     * Возвращает массив вопросов и ответов для всех раундов.
     * Каждый внутренний массив содержит пару [вопрос, правильный ответ].
     *
     * @return двумерный массив строк с вопросами и ответами
     */
    String[][] getQuestionsAndAnswers();
}
